                    සදහා පෙනී සිටින සිසුන්ට දැනුම ගුණාත්මක ලෙස ලබා දීම. තරග විභාග සදහා පෙනී සිටින සිසුන්ට දැනුම ගුණාත්මක
                    ලෙස ලබා දීම. තරග විභාග සදහා පෙනී සිටින සිසුන්ට දැනුම ගුණාත්මක ලෙස ලබා දීම. තරග විභාග සදහා පෙනී සිටින
